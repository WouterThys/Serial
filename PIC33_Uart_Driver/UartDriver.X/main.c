static void initialize();

void initialize() {
    sysInterruptEnable(false);
    
    // Initialize system
    sysInitPll();
    sysInitOscillator();
    sysInitPorts();
    
    // Interrupts
    sysInitInterrupts();
    sysInterruptEnable(true);
}

/*******************************************************************************
 *          VARIABLES
 ******************************************************************************/

/*******************************************************************************
 *          MAIN PROGRAM
 ******************************************************************************/

int main(void) {

    initialize();
    
    
    uartInit(UART_MODULE_1, UART1_BAUD);
    uartEnable(UART_MODULE_1, true);
    
    DelayMs(1000);
    
    while(1) {
    
        
        
    }
}


